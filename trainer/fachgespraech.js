function zufall(array) {
  return array[Math.floor(Math.random() * array.length)];
}

function fachgespraechStarten() {
  const fall = zufall(fachgespraechFaelle);
  const app = document.getElementById("fachgespraech-app");

  app.innerHTML = `
    <section class="fg-card">
      <div class="fg-kopf">
        <span class="fg-badge">Industriemeister Metall · HQ</span>
        <h2>Fachgespräch: ${fall.titel}</h2>
        <p>${fall.dauer}</p>
      </div>

      <h3>Ausgangssituation</h3>
      <p class="fg-text">${fall.situation.trim()}</p>

      <h3>Ihr Handlungsauftrag</h3>
      <p class="fg-auftrag">${fall.auftrag.trim()}</p>

      <label for="fg-notizen"><strong>Ihre Lösungsstruktur / Notizen</strong></label>
      <textarea id="fg-notizen" rows="10"
        placeholder="1. Situation analysieren ...&#10;2. Ziele festlegen ...&#10;3. Maßnahmen begründen ...&#10;4. Umsetzung und Kontrolle ..."></textarea>

      <div class="fg-aktionen">
        <button type="button" class="fg-button" id="fg-loesung-button">
          Lösungsschwerpunkte anzeigen
        </button>
        <button type="button" class="fg-button fg-button-neu" id="fg-neu-button">
          Neuen Fall ziehen
        </button>
      </div>

      <div id="fg-auswertung" hidden>
        <h3>Lösungsschwerpunkte</h3>
        <ol>
          ${fall.loesung.map(punkt => `<li>${punkt}</li>`).join("")}
        </ol>

        <h3>Typische Prüfer-Rückfragen</h3>
        <ol>
          ${fall.rueckfragen.map(frage => `<li>${frage}</li>`).join("")}
        </ol>

        <p class="fg-hinweis">
          Selbstcheck: Ist Ihre Lösung nachvollziehbar aufgebaut? Begründen Sie jede
          Maßnahme mit Auswirkungen auf Mitarbeitende, Qualität, Termine, Kosten und
          Arbeitssicherheit.
        </p>
      </div>
    </section>
  `;

  document.getElementById("fg-loesung-button").addEventListener("click", () => {
    document.getElementById("fg-auswertung").hidden = false;
  });

  document.getElementById("fg-neu-button").addEventListener("click", fachgespraechStarten);
}

document.addEventListener("DOMContentLoaded", () => {
  const trainer = document.querySelector("main") || document.body;

  const bereich = document.createElement("section");
  bereich.className = "fg-bereich";
  bereich.innerHTML = `
    <h2>Führung & Personal – Fachgespräch</h2>
    <p>
      Trainiere eine mündliche Situationsaufgabe: Situation analysieren,
      Maßnahmen begründen und auf Rückfragen reagieren.
    </p>
    <button type="button" class="fg-button" id="fg-start-button">
      Fachgespräch starten
    </button>
    <div id="fachgespraech-app"></div>
  `;

  trainer.appendChild(bereich);
  document.getElementById("fg-start-button").addEventListener("click", fachgespraechStarten);
});
